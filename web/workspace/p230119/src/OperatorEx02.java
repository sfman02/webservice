
public class OperatorEx02 {

	public static void main(String[] args) {

		//관계 연산자
		//
		int n1 = 5, n2 = 3;
		System.out.println("크니? " + (n1 > n2));
		System.out.println("다르니? " + (n1 != n2));
		System.out.println("같니? " + (n1 == n2));
		// 논리 연산자
		System.out.println(+ n1 > 4 && n2> 4);
		System.out.println(+ n1 > 4 || n2> 4);
		System.out.println(! (n1 > 4 ));
		
	}

}
